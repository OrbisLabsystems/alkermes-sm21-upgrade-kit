  SELECT MLP_COMPONENTS.PRODUCT_ID,   
         ltrim(MLP_COMPONENTS.PRODUCT_VERSION),   
         MLP_COMPONENTS.ANALYSIS_ID,   
         MLP_COMPONENTS.COMPONENT_NAME,   
         ltrim(MLP_COMPONENTS.PRODUCT_NUMBER),   
         MLP_COMPONENTS.COMPONENT_TYPE,   
         MLP_COMPONENTS.UNITS,   
         MLP_COMPONENTS.PLACES,   
         MLP_COMPONENTS.TYPICAL_TEXT,   
         ltrim(MLP_COMPONENTS.ENTRY_CODE),   
         ltrim(MLP_COMPONENTS.ORDER_NUM),   
         ltrim(MLP_VALUES.ENTRY_CODE),   
         MLP_VALUES.LEVEL_ID,   
         MLP_VALUES.MIN_LIMIT,   
         MLP_VALUES.MAX_LIMIT,   
         MLP_VALUES.TEXT_SPEC,   
         MLP_VALUES.CALCULATION,   
         MLP_VALUES.TEXT_PHRASE,   
         MLP_VALUES.BOOLEAN_PASS,   
         MLP_VALUES.AQL_VALUE  
    FROM MLP_COMPONENTS,   
         MLP_VALUES   
ORDER BY MLP_COMPONENTS.ENTRY_CODE,MLP_VALUES.ENTRY_CODE ;